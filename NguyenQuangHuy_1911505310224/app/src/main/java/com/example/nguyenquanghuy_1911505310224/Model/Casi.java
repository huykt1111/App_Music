package com.example.nguyenquanghuy_1911505310224.Model;

public class Casi {
    public String ten_casi;
    public int hinh_casi;

    public Casi(String ten_casi, int hinh_casi) {
        this.ten_casi = ten_casi;
        this.hinh_casi = hinh_casi;
    }

    public String getTen_casi() {
        return ten_casi;
    }

    public void setTen_casi(String ten_casi) {
        this.ten_casi = ten_casi;
    }

    public int getHinh_casi() {
        return hinh_casi;
    }

    public void setHinh_casi(int hinh_casi) {
        this.hinh_casi = hinh_casi;
    }
}
